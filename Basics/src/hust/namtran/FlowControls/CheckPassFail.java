package hust.namtran.FlowControls;

public class CheckPassFail {
	public static void main(String[] args) {
		int mark = 49;
		System.out.println("This is mark " + mark);
		if (mark >= 50) {
			System.out.println("PASS");
		} else {
			System.out.println("FAIL");
		}
		System.out.println("DONE");
	}
}

package other;

import java.util.Scanner;

public class Solution {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int T = Integer.parseInt(sc.next());
		for (int t = 1; t <= T; t++) {
			System.out.println("#" + t);
		}
		// print
		sc.close();
	}

}
